#define FONT_W   8
#define FONT_H   16

// ─── Console state ────────────────────────────────────────────────────────────
static u64 fb_addr   = 0;
static u32 fb_width  = 0;
static u32 fb_height = 0;
static u32 fb_pitch  = 0;
static u16 fb_bpp    = 0;
static u8  fb_red_mask_size   = 8;
static u8  fb_red_mask_shift  = 16;
static u8  fb_green_mask_size = 8;
static u8  fb_green_mask_shift = 8;
static u8  fb_blue_mask_size  = 8;
static u8  fb_blue_mask_shift = 0;
static u8  fb_bytes_per_pixel = 4;

static u32 cursor_x  = 0;
static u32 cursor_y  = 0;
static u32 fg_color  = FB_WHITE;
static u32 bg_color  = FB_BLACK;

static u32 cols = 0; // text columns
static u32 rows = 0; // text rows

// ─── Pixel operations ─────────────────────────────────────────────────────────
static inline u32 mask_for_size(u8 bits) {
    if (bits == 0) return 0;
    if (bits >= 32) return 0xFFFFFFFFu;
    return (1u << bits) - 1u;
}

static inline u32 scale_8_to_n(u32 c8, u8 bits) {
    u32 maxv = mask_for_size(bits);
    if (maxv == 0) return 0;
    if (maxv == 255) return c8;
    return (c8 * maxv + 127u) / 255u;
}

static inline u32 pack_color(u32 rgb) {
    u32 r = (rgb >> 16) & 0xFFu;
    u32 g = (rgb >> 8) & 0xFFu;
    u32 b = rgb & 0xFFu;

    u32 pr = scale_8_to_n(r, fb_red_mask_size)   << fb_red_mask_shift;
    u32 pg = scale_8_to_n(g, fb_green_mask_size) << fb_green_mask_shift;
    u32 pb = scale_8_to_n(b, fb_blue_mask_size)  << fb_blue_mask_shift;
    return pr | pg | pb;
}

void fb_putpixel(u32 x, u32 y, u32 color) {
    if (!fb_addr || x >= fb_width || y >= fb_height) return;
    u8 *pixel = (u8 *)(fb_addr + (u64)y * fb_pitch + (u64)x * fb_bytes_per_pixel);
    u32 packed = pack_color(color);
    for (u8 i = 0; i < fb_bytes_per_pixel; i++) {
        pixel[i] = (u8)(packed >> (8u * i));
    }
}

void fb_fill_rect(u32 x, u32 y, u32 w, u32 h, u32 color) {
    if (!fb_addr) return;

    u32 y_end = (y + h < fb_height) ? (y + h) : fb_height;
    u32 x_end = (x + w < fb_width) ? (x + w) : fb_width;
    if (x >= x_end || y >= y_end) return;

    if (fb_bytes_per_pixel == 4) {
        u32 packed = pack_color(color);
        for (u32 row = y; row < y_end; row++) {
            u32 *line = (u32 *)(fb_addr + (u64)row * fb_pitch + (u64)x * 4);
            for (u32 col = x; col < x_end; col++) {
                line[col - x] = packed;
            }
        }
        return;
    }

    for (u32 row = y; row < y_end; row++) {
        for (u32 col = x; col < x_end; col++) {
            fb_putpixel(col, row, color);
        }
    }
}

void fb_clear(u32 color) {
    if (!fb_addr) return;
    fb_fill_rect(0, 0, fb_width, fb_height, color);
    cursor_x = 0;
    cursor_y = 0;
}

// ─── Init ─────────────────────────────────────────────────────────────────────
void fb_init(u64 addr, u32 width, u32 height, u32 pitch, u16 bpp,
             u8 red_mask_size, u8 red_mask_shift,
             u8 green_mask_size, u8 green_mask_shift,
             u8 blue_mask_size, u8 blue_mask_shift) {
    fb_addr   = addr;
    fb_width  = width;
    fb_height = height;
    fb_pitch  = pitch;
    fb_bpp    = bpp;
    fb_bytes_per_pixel = (u8)((bpp + 7) / 8);
    if (fb_bytes_per_pixel == 0) fb_bytes_per_pixel = 1;
    if (fb_bytes_per_pixel > 4) fb_bytes_per_pixel = 4;

    fb_red_mask_size    = red_mask_size;
    fb_red_mask_shift   = red_mask_shift;
    fb_green_mask_size  = green_mask_size;
    fb_green_mask_shift = green_mask_shift;
    fb_blue_mask_size   = blue_mask_size;
    fb_blue_mask_shift  = blue_mask_shift;

    if (fb_red_mask_size == 0 && fb_green_mask_size == 0 && fb_blue_mask_size == 0) {
        // Conservative fallback for firmware that does not report masks.
        fb_red_mask_size = fb_green_mask_size = fb_blue_mask_size = 8;
        fb_red_mask_shift = 16;
        fb_green_mask_shift = 8;
        fb_blue_mask_shift = 0;
    }

    cols = width  / FONT_W;
    rows = height / FONT_H;
    fb_clear(FB_BLACK);
}

int fb_getwidth(void)  { return (int)fb_width; }
int fb_getheight(void) { return (int)fb_height; }

void fb_set_color(u32 fg, u32 bg) { fg_color = fg; bg_color = bg; }
void fb_set_panic_colors(void) { fb_set_color(FB_WHITE, FB_RED); fb_clear(FB_RED); }

// ─── Character rendering ──────────────────────────────────────────────────────
static void draw_char(u32 cx, u32 cy, char c, u32 fg, u32 bg) {
    u8 idx = (u8)c;
    if (idx > 0x7F) idx = '?';
    const u8 *glyph = font_data[idx];
    u32 px = cx * FONT_W;
    u32 py = cy * FONT_H;
    for (u32 row = 0; row < FONT_H; row++) {
        u8 bits = glyph[row];
        for (u32 col = 0; col < FONT_W; col++) {
            u32 color = (bits & (0x80 >> col)) ? fg : bg;
            fb_putpixel(px + col, py + row, color);
        }
    }
}

static void fb_scroll(void) {
    // Move all rows up by one character height
    u32 copy_bytes = fb_pitch * (fb_height - FONT_H);
    u8 *dst = (u8 *)fb_addr;
    u8 *src = (u8 *)fb_addr + fb_pitch * FONT_H;
    memmove(dst, src, copy_bytes);
    // Clear last row
    fb_fill_rect(0, fb_height - FONT_H, fb_width, FONT_H, bg_color);
}

void fb_putchar(char c) {
    if (!fb_addr) return;
    if (c == '\n') {
        cursor_x = 0;
        cursor_y++;
    } else if (c == '\r') {
        cursor_x = 0;
    } else if (c == '\b') {
        if (cursor_x > 0) {
            cursor_x--;
            draw_char(cursor_x, cursor_y, ' ', fg_color, bg_color);
        }
    } else if (c == '\t') {
        u32 next = (cursor_x + 8) & ~7u;
        while (cursor_x < next && cursor_x < cols) {
            draw_char(cursor_x, cursor_y, ' ', fg_color, bg_color);
            cursor_x++;
        }
    } else {
        draw_char(cursor_x, cursor_y, c, fg_color, bg_color);
        cursor_x++;
        if (cursor_x >= cols) {
            cursor_x = 0;
            cursor_y++;
        }
    }
    if (cursor_y >= rows) {
        fb_scroll();
        cursor_y = rows - 1;
    }
}

void fb_puts(const char *s) {
    while (*s) fb_putchar(*s++);
}

// ─── Boot screen ──────────────────────────────────────────────────────────────
void fb_draw_boot_screen(void) {
    if (!fb_addr) return;

    // Polish flag: top half white, bottom half crimson
    u32 flag_height = fb_height / 4; // Use top quarter for flag banner
    fb_fill_rect(0, 0, fb_width, flag_height / 2, FB_POLISH_WHITE);
    fb_fill_rect(0, flag_height / 2, fb_width, flag_height / 2, FB_POLISH_RED);

    // Fill rest with dark background
    fb_fill_rect(0, flag_height, fb_width, fb_height - flag_height, 0x1A1A2E);

    // Position text console below flag
    cursor_x = 0;
    cursor_y = (flag_height / FONT_H) + 1;
    bg_color = 0x1A1A2E;
    fg_color = FB_WHITE;

    // Center helpers
    u32 center_col = cols / 2;

    // ASCII White Eagle art (simplified)
    static const char *eagle[] = {
        "          .~.          ",
        "         (   )         ",
        "      .-' \\_/ '-.      ",
        "     /  O     O  \\     ",
        "    |    _____    |    ",
        "    |   /     \\   |    ",
        "     \\_/ ORZEL \\_/     ",
        "      |  KERNEL |      ",
        "      |_________|      ",
        "       /       \\       ",
        "      /  POLAND \\      ",
        NULL
    };

    for (int i = 0; eagle[i]; i++) {
        u32 len = (u32)strlen(eagle[i]);
        u32 col = (center_col > len/2) ? (center_col - len/2) : 0;
        cursor_x = col;
        fg_color = FB_POLISH_WHITE;
        fb_puts(eagle[i]);
        cursor_x = 0;
        cursor_y++;
    }

    cursor_y++;
    fg_color = FB_YELLOW;

    // Center "PolandOS" title
    const char *title = "  PolandOS v0.0.1 -- Orzel Kernel  ";
    u32 tlen = (u32)strlen(title);
    cursor_x = (center_col > tlen/2) ? (center_col - tlen/2) : 0;
    fb_puts(title);
    cursor_x = 0;
    cursor_y++;

    fg_color = FB_LGRAY;
    const char *sub = "  Dla Polski. Dla wolnosci. Dla kodu.  ";
    u32 slen = (u32)strlen(sub);
    cursor_x = (center_col > slen/2) ? (center_col - slen/2) : 0;
    fb_puts(sub);
    cursor_x = 0;
    cursor_y += 2;

    // Reset for normal console use
    fg_color = FB_WHITE;
    bg_color = 0x1A1A2E;
}
